package java_Classes.day2_printing_comments;

public class Concatenation {

	public static void main(String[] args) {
		
		System.out.println("Hi, My Number is 100");
		System.out.println("Hi, My Number is " + "100");
		System.out.println("Hi, My Number is " + 100);
		
		int number = 100;
		System.out.println("Hi, My Number is " + number);
		
		

	}

}
